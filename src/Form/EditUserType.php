    {
        $builder
            ->add('username', TextType::class, array('label' => 'Vartotojo vardas'))
            ->add('email', EmailType::class, array('label' => 'El.PaÅ¡tas'))
            ->add('roles',ChoiceType::class, [
                'multiple' => true,
